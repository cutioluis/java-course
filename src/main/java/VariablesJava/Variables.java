package VariablesJava;

public class Variables {
    public static void main(String args[]) {
        System.out.println("Variables");

        // Tipo de dato entero
        // int > nameVar > = > valor
        int miPrimerVariable = 17;
        System.out.println(miPrimerVariable);
        miPrimerVariable = 18;
        System.out.println(miPrimerVariable);

        // Tipo de dato String
        // String >> nameVar > = > valor
        String miCadenaVar = "Hola mundo";
        System.out.println(miCadenaVar);
        miCadenaVar = "Otro Hola Mudno";
        System.out.println(miCadenaVar);
    }
}
