package SentenciasControl;

public class SentenciaIfElse {
   public static void main(String[] args) {
      // Sentencia if
      boolean condicion = false;

      // Recive la condicion si es true la ejecuta el codigo 
      // Si no es true no ejecuta el otro ocdigo

      if (condicion) {
         System.out.println("Si la condicion es true imprime este mensaje");
      } else {
         System.out.println("Si no es true no immrime esto");
      }
   }
   
}
