package operadores;

public class OperadoresAritmeticos {
   // Operatores aritmeticos: +, -, *, / =  los tipicos de la escuela
   public static void main(String[] args)  {
      // suma 
      System.out.println(2 + 2);
      // resta 
      System.out.println(2 - 2);
      // multiplication
      System.out.println(2 * 2);
      // division
      System.out.println(2 / 2);
      // Restp = si no sabes que ser nos da el resto de una division por ejemplo 
      // 10 / 10 seria 1 => pero no sobra nada entoces el resto es 0
      // 10 / 2 seria  5 => el resto es 5
      System.out.println(10 / 10);
      System.out.println(10 / 2);


   }
}
